    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomb,curp,ref,puesto,contr;
        int edad,puest;
        double sueld;
        nomb=jTextField1.getText();
        curp=jTextField2.getText();
        edad=Integer.parseInt(jTextField3.getText());
        ref=jTextArea1.getText();                                     
        sueld=Double.parseDouble(jTextField4.getText());
        contr=jTextField5.getText();
        Connection conn = conexion.getConnection(); //Para tener conexi√≥n a la Base de Datos.
        String sql="INSERT INTO empleados(no_de_empleado,nombre,curp,edad,referencias,puesto,sueldo,contrase√±a,mesas,horario) VALUES (?,?,?,?,?,?,?,?,?,?)";
        puesto=(String) jComboBox1.getSelectedItem();
        if (puesto!="Selecciona un puesto"){
        try{
            PreparedStatement pst  = conn.prepareStatement(sql);
            pst.setString(1, null);
            pst.setString(2, nomb);
            pst.setString(3, curp);
            pst.setInt(4, edad);
            pst.setString(5, ref);
            pst.setString(6, puesto);
            pst.setDouble(7, sueld);
            pst.setString(8, contr);
            pst.setString(9, "");
            pst.setString(10, "");
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Empleado registrado");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);              //Se vacian al registrar
            jTextArea1.setText("");
            jTextField5.setText("");
        }
        }
            catch(Exception e){
            JOptionPane.showMessageDialog(this, "El error es:"+ e.getMessage());
        }    
        }else{
            JOptionPane.showMessageDialog(null,"Puesto erroneo","Error",JOptionPane.WARNING_MESSAGE);
        }
            
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

